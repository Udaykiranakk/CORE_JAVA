package practice;

import java.util.Date;

public class StudentReg {

	private String studentId;
	private String courseName;
	private String courseDuration;
	private String nameOfTheBranch;
	private String nameOfTheSubBranch;
	private Date dateOfJoining;
	
	 
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseDuration() {
		return courseDuration;
	}
	public void setCourseDuration(String courseDuration) {
		this.courseDuration = courseDuration;
	}
	public String getNameOfTheBranch() {
		return nameOfTheBranch;
	}
	public void setNameOfTheBranch(String nameOfTheBranch) {
		this.nameOfTheBranch = nameOfTheBranch;
	}
	public String getNameOfTheSubBranch() {
		return nameOfTheSubBranch;
	}
	public void setNameOfTheSubBranch(String nameOfTheSubBranch) {
		this.nameOfTheSubBranch = nameOfTheSubBranch;
	}
	public Date getDateOfJoining() {
		return dateOfJoining;
	}
	public void setDateOfJoining(Date dateOfJoining) {
		this.dateOfJoining = dateOfJoining;
	}
	
	
}
